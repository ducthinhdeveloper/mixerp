DROP FUNCTION IF EXISTS policy.is_elevated_user(_user_id integer);

CREATE FUNCTION policy.is_elevated_user(_user_id integer)
RETURNS boolean
AS
$$
BEGIN
    IF EXISTS
    (
        SELECT 0 FROM office.users
        WHERE user_id = $1
        AND elevated
    ) THEN
        RETURN true;
    END IF;

    RETURN false;
END;
$$
LANGUAGE plpgsql;


